package Aula01;

public class OperadorExemplo {

	public static void main(String[] args) {
		int x =10;
		System.out.println(x++); //print o valor de x e soma +1
		System.out.println(++x); // soma +1 e print
		System.out.println(x--); //print o valor de x e -1 
		System.out.println(--x);// subtrai o valor de x e print
	}

}
